const fs = require('fs');
const path = require('path');

const facesDir = path.join(__dirname, '../src/assets/faces/high_end');
const indexFile = path.join(facesDir, 'index.js');

const files = fs.readdirSync(facesDir)
  .filter(file => file.startsWith('face_') && file.endsWith('.png'))
  .sort(); // Sorting ensures deterministic order

let content = `// Auto-generated by scripts/generate-faces-index.cjs\n\n`;

files.forEach((file, idx) => {
  content += `import face_${String(idx + 1).padStart(2, '0')} from './${file}';\n`;
});

content += `\nexport const HIGH_END_FACES = [\n`;
files.forEach((file, idx) => {
  content += `  face_${String(idx + 1).padStart(2, '0')},\n`;
});
content += `];\n`;

fs.writeFileSync(indexFile, content, 'utf8');
console.log(`Generated index.js with ${files.length} faces.`);
